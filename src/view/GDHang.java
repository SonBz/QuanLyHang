   public void bang(){
       model.setRowCount(0);
       for(hang h:list){
           model.addRow(new Object[]{
           h.getMahang(),h.getTenhang(),h.getDovitinh(),h.getDongia(),h.getSohang()
           });
       }
   }
   public void hienthi(){
       jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
           @Override
           public void valueChanged(ListSelectionEvent e) {
             int r=jTable1.getSelectedRow();
             if(r!=-1){
                 txtMh.setText(model.getValueAt( r, 0).toString());
                 txtTen.setText(model.getValueAt(r, 1).toString());
                 cbDvt.setSelectedItem(model.getValueAt(r, 2));
                 txtDg.setText(model.getValueAt(r, 3).toString());               
                 txtSh.setText(model.getValueAt(r, 4).toString());
             }
           }
       });
   }
