    public boolean kt(){
        
        if(txtMh.getText().equals("")){
           jlbMh.setText("Chưa Nhập mã");
            return true;
        }
        else if(txtTen.getText().equals("")){
            jlbTenhang.setText("Chưa Nhập Tên");
            return true;
        } else if(txtDg.getText().equals("")){
            jlbDongia.setText("Chưa Nhập Đơn Giá");
            return true;
        }
        else if(txtSh.getText().equals("")){
            jlbSoHang.setText("Chưa Nhập Số Hàng");
            return true;
        }
        for(int i=0;i<list.size();i++){
            String str=list.get(i).getMahang();
            if(txtMh.getText().equalsIgnoreCase(str)){
                jlbMh.setText("Trùng mã");
                return true;
            }
        }
        return false;
    }
